package dam.pmpd.pokemon_pmpd03_javl.models;

public class PokemonResult {
    private String name;
    private String url;

    // Getters y Setters
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }
}
